//! ブロックチェーンログ記録ユーティリティ
//!
//! このモジュールはブロックチェーンアプリケーション用のログ記録ユーティリティを提供し、
//! コンパイル時に有効または無効にできる条件付きデバッグ出力を
//! 実行時のオーバーヘッドなしで実現します。

const std = @import("std");

/// コンパイル時にデバッグログを有効または無効にするフラグ
///
/// trueに設定すると、デバッグログメッセージが出力されます。
/// falseに設定すると、デバッグログは実行時のオーバーヘッドなしで
/// コンパイル時に完全に除去されます。
pub const debug_logging = false;

/// デバッグログが有効な場合にデバッグログメッセージを出力する
///
/// この関数はdebug_loggingフラグに基づいて条件付きでデバッグメッセージを出力します。
/// この条件はコンパイル時に評価されるため、デバッグログが無効な場合には
/// ゼロオーバーヘッドとなります。
///
/// 引数:
///     format: ログメッセージのフォーマット文字列
///     args: フォーマット文字列に挿入する値
///
/// 注意:
///     comptimeを使用してdebug_loggingがfalseの場合に関数呼び出しを
///     完全に除去するため、本番ビルドでの実行時オーバーヘッドはありません。
pub fn debugLog(comptime format: []const u8, args: anytype) void {
    if (comptime debug_logging) {
        std.debug.print(format, args);
    }
}
