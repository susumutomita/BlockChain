//! ブロックチェーンエラー定義
//!
//! このモジュールはブロックチェーンアプリケーション全体で使用される
//! エラータイプを定義します。共通のエラーセットを使用することで、
//! 一貫したエラー処理パターンを維持し、モジュール間のエラー伝播を
//! 簡素化します。

/// ブロックチェーン固有の操作のためのChainErrorエラーセット
///
/// データ検証、解析、フォーマットチェックなどのブロックチェーン操作に
/// 関連するエラーを含みます。
///
/// エラー:
///     InvalidHexLength: 16進数文字列の長さが無効（偶数でない）
///     InvalidHexChar: 16進数文字列に無効な文字が含まれる
///     InvalidFormat: 一般的なフォーマット検証失敗（JSONなど）
pub const ChainError = error{
    /// 16進数文字列の長さが無効（偶数である必要がある）
    InvalidHexLength,

    /// 16進数文字列に有効範囲外の文字が含まれる（0-9, a-f, A-F）
    InvalidHexChar,

    /// データ構造やシリアル化されたコンテンツの一般的なフォーマットエラー
    InvalidFormat,
};
